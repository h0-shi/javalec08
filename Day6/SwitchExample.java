
public class SwitchExample {

	public static void main(String args[]) {
		
		String alpha = "서울";
		
		switch(alpha) {
		
		case "서울" :
			System.out.println("우리 집으로 가자");
				break;
				
		case "제주" :
			System.out.println("비행기 타러 가자");
				break;
				
		default :
			System.out.println("집이 없어");
		
		}
		
	}
	
}
